# Shared metric helpers (same definitions used in Phase 4)

def normed_ed(gt: str, pred: str) -> float:
    """Levenshtein distance / max(len(gt), len(pred)). Bounded in [0, 1]."""
    gt = '' if pd.isna(gt) else str(gt)
    pred = '' if pd.isna(pred) else str(pred)
    if not gt and not pred:
        return 0.0
    return editdistance.eval(gt, pred) / max(len(gt), len(pred), 1)

def token_f1(gt: str, pred: str) -> float:
    """Token-set F1 (unordered, deduplicated tokens)."""
    gt = '' if pd.isna(gt) else str(gt)
    pred = '' if pd.isna(pred) else str(pred)
    if not gt or not pred:
        return 0.0
    gt_set = set(gt.split())
    pr_set = set(pred.split())
    common = gt_set & pr_set
    if not common:
        return 0.0
    p = len(common) / len(pr_set)
    r = len(common) / len(gt_set)
    return 2 * p * r / (p + r)

