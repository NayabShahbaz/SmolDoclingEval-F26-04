# Parse OTSL output to count rows, columns, headers, spans
# OTSL grammar: <ched>, <fcel>, <ecel>, <lcel>, <ucel>, <xcel>, <nl>

def count_otsl_structure(otsl: str) -> dict:
    if pd.isna(otsl) or not otsl:
        return {'n_rows': 0, 'n_cols': 0, 'n_headers': 0, 'n_spans': 0}

    # Strip <loc_x> tags (don't affect structure)
    s = re.sub(r'<loc_\d+>', '', str(otsl))

    n_rows  = s.count('<nl>')
    n_headers = s.count('<ched>')
    n_spans = s.count('<lcel>') + s.count('<ucel>') + s.count('<xcel>')

    # Columns = number of cell tokens in the first row
    first_row = re.search(r'<otsl>(.*?)<nl>', s, re.DOTALL)
    if first_row:
        n_cols = len(re.findall(r'<(ched|fcel|ecel|lcel|ucel|xcel)>', first_row.group(1)))
    else:
        n_cols = 0

    return {'n_rows': n_rows, 'n_cols': n_cols, 'n_headers': n_headers, 'n_spans': n_spans}

